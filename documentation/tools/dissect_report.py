def heading_level(text: str, is_first_in_group: bool) -> int:
    if is_first_in_group:
        return 1
    text = clean_heading_text(text)
    if text.startswith("Chapter ") or text.startswith("CHAPTER ") or text == "References":
        return 1
    if text in {"Acknowledgement", "List of Tables", "List of Figures", "List of Abbreviations", "Abstract"}:
        return 1
    if text == "Table of Contents":
        return 1
    if text in ACTIVITY_TITLES or text.startswith("Architecture Overview"):
        return 2
    match = re.match(r"^(\d+(?:\.\d+)*)", text)
    if match:
        depth = len(match.group(1).split("."))
        return min(max(depth, 1), 6)
    if re.match(r"^[A-Z]\.", text):
        return 2
    return 2


def normalize_inline_text(text: str) -> str:
    text = normalize_text(text)
    return text.replace(" .", ".").replace(" ,", ",").replace(" ;", ";").replace(" :", ":")


def escape_table_cell(value: str) -> str:
    return value.replace("|", "\\|")


def format_paragraph(text: str) -> str:
    if not text:
        return ""
    if text == "Image":
        return ""
    if re.fullmatch(r"\d+", text):
        return ""
    if text.startswith("Figure ") or text.startswith("Table "):
        return f"*{text}*\n"
    return f"{text}\n"


def convert_list(tag: Tag) -> str:
    items: list[str] = []
    for index, li in enumerate(tag.find_all("li", recursive=False), start=1):
        text = normalize_inline_text(li.get_text(" ", strip=True))
        if not text:
            continue
        prefix = f"{index}." if tag.name == "ol" else "-"
        items.append(f"{prefix} {text}")
    if not items:
        return ""
    return "\n".join(items) + "\n"


def convert_table(tag: Tag) -> str:
    rows: list[list[str]] = []
    for tr in tag.find_all("tr"):
        cells = tr.find_all(["th", "td"])
        row = [normalize_inline_text(cell.get_text(" ", strip=True)) for cell in cells]
        if any(row):
            rows.append(row)
    if not rows:
        return ""

    width = max(len(row) for row in rows)
    normalized_rows = [row + [""] * (width - len(row)) for row in rows]
    header = normalized_rows[0]
    if len(normalized_rows) == 1:
        header = [f"Column {i}" for i in range(1, width + 1)]
        normalized_rows.insert(0, header)

    lines = [
        "| " + " | ".join(escape_table_cell(cell) for cell in normalized_rows[0]) + " |",
        "| " + " | ".join("---" for _ in normalized_rows[0]) + " |",
    ]
    for row in normalized_rows[1:]:
        lines.append("| " + " | ".join(escape_table_cell(cell) for cell in row) + " |")
    return "\n".join(lines) + "\n"


def convert_image(tag: Tag, current_output: Path) -> str:
    src = (tag.get("src") or "").strip()
    if not src:
        return ""
    image_name = Path(src).name
    target = SOURCE_IMAGE_DIR / image_name
    proper_rel = Path(os.path.relpath(target, start=current_output.parent))
    alt = f"Source figure {image_name}"
    return f"![{alt}]({proper_rel.as_posix()})\n"


def render_block(tag: Tag, current_group: str) -> str:
    text = normalize_inline_text(tag.get_text(" ", strip=True))
    if tag.name == "p":
        return format_paragraph(text)
    if tag.name in {"ul", "ol"}:
        return convert_list(tag)
    if tag.name == "table":
        return convert_table(tag)
    return ""


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n", encoding="utf-8")


def build_sections(source_html: Path) -> tuple[dict[str, str], dict[str, list[str]]]:
    soup = BeautifulSoup(source_html.read_text(encoding="utf-8", errors="ignore"), "html.parser")
    sections: dict[str, list[str]] = defaultdict(list)
    section_headings: dict[str, list[str]] = defaultdict(list)
    current_group = "cover"

    body_children = [node for node in soup.body.children if isinstance(node, Tag)]

    for tag in body_children:
        nested_images = tag.find_all("img") if tag.name != "img" else []

        if tag.name in {"h1", "h2", "h3", "h4", "h5", "h6"}:
            text = clean_heading_text(tag.get_text(" ", strip=True))
            if not text:
                if current_group in SECTION_PATHS:
                    for image in nested_images:
                        sections[current_group].append(convert_image(image, SECTION_PATHS[current_group]))
                continue
            mapped_group = group_for_heading(text)
            if mapped_group:
                current_group = mapped_group
            if current_group not in SECTION_PATHS:
                continue
            level = heading_level(text, not sections[current_group])
            sections[current_group].append(f'{"#" * level} {text}\n')
            section_headings[current_group].append(text)
            for image in nested_images:
                sections[current_group].append(convert_image(image, SECTION_PATHS[current_group]))
            continue

        if tag.name == "p":
            text = normalize_inline_text(tag.get_text(" ", strip=True))
            if not text:
                if current_group in SECTION_PATHS:
                    for image in nested_images:
                        sections[current_group].append(convert_image(image, SECTION_PATHS[current_group]))
                continue
            if text == "Table of Contents":
                current_group = "toc_lists"
                level = heading_level(text, not sections[current_group])
                sections[current_group].append(f'{"#" * level} {text}\n')
                section_headings[current_group].append(text)
                continue
            if (
                current_group in {"ch4", "4.1-4.2", "4.3.1"}
                and (text.startswith("A. Architecture Overview") or text.startswith("1. Architecture Overview"))
            ):
                current_group = "4.3.1"
                level = heading_level("Architecture Overview (4-Tier Layered SOA)", not sections[current_group])
                sections[current_group].append(f'{"#" * level} Architecture Overview (4-Tier Layered SOA)\n')
                section_headings[current_group].append("Architecture Overview (4-Tier Layered SOA)")
                continue

        if current_group not in SECTION_PATHS:
            continue

        if tag.name == "img":
            sections[current_group].append(convert_image(tag, SECTION_PATHS[current_group]))
            continue

        for image in nested_images:
            sections[current_group].append(convert_image(image, SECTION_PATHS[current_group]))

        block = render_block(tag, current_group)
        if block:
            sections[current_group].append(block)

    materialized = {key: "\n".join(value).strip() for key, value in sections.items() if value}
    return materialized, section_headings


def build_manifest(section_headings: dict[str, list[str]]) -> dict[str, object]:
    ordered_files = []
    for key in MANIFEST_ORDER:
        path = SECTION_PATHS[key]
        if path.exists():
            ordered_files.append(
                {
                    "key": key,
                    "path": path.relative_to(DOC_ROOT).as_posix(),
                    "headings": section_headings.get(key, []),
                }
            )

    return {
        "title": "National Vaccination and Outbreak Monitoring System for Ethiopia",
        "source_html": next((p.name for p in SOURCE_ASSET_DIR.glob("*.html")), None),
        "ordered_files": ordered_files,
    }


